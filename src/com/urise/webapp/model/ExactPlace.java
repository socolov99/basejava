package com.urise.webapp.model;

public class ExactPlace {
    private String exactPlaceName;
    private String period;
    private String exactPlaceDescription;

    public String getExactPlaceName() {
        return exactPlaceName;
    }

    public void setExactPlaceName(String exactPlaceName) {
        this.exactPlaceName = exactPlaceName;
    }

    public String getPeriod() {
        return period;
    }

    public void setPeriod(String period) {
        this.period = period;
    }

    public String getExactPlaceDescription() {
        return exactPlaceDescription;
    }

    public void setExactPlaceDescription(String exactPlaceDescription) {
        this.exactPlaceDescription = exactPlaceDescription;
    }
}
